#include "stdlib.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;

	i = 1;
	while (src[i])
		i++;
	if (!(dest = (char*)malloc(i + 1)))
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
